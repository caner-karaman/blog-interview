import React, {useEffect} from 'react';
import type { NextPage } from 'next';
import { gql } from 'graphql-tag';
import { useQuery } from '@apollo/client';
import Head from 'next/head';
import Header from "../components/header/Header";
import Sidebar from '../components/sidebar/Sidebar';
import BlogItem from '../components/blogItem/BlogItem';
import { ItemWrapper, StyledContainer, Main, Articles, FilterWrapper } from './App.styles';
import { useRouter } from 'next/router';
import Pagination from '../components/pagination/Pagination';

const ALL_POSTS_QUERY = gql`
  query ALL_POSTS_QUERY($page: Int!) {
    posts(options: {
    paginate: {
      page: $page
      limit: 4
    }
  }) {
    data {
      id
      title
      body
      comments {
        data {
          id
          body
          name
        }
      }
    }
    meta {
      totalCount
    }
  }
  }
`;

const SINGLE_USER_QUERY = gql`
  query SINGLE_USER_QUERY($id: ID!, $page: Int!) {
    user(id: $id) {
        name
        username
        email
        id
        posts(options: {
          paginate: {
            page: $page
            limit: 4
          }
        }) {
          data {
            title
            id
            body
            comments {
              data {
                body 
                name
                id
              }
            }
          }
          meta {
            totalCount
          }
        }
    }
  }
`;

const Home: NextPage = () => {
  const { query } = useRouter();
  const id = query?.user;
  const page = query?.page as string || '1';
  const Query = id ? SINGLE_USER_QUERY : ALL_POSTS_QUERY;
  const QUERY_OPTIONS = {
    variables: {
      id,
      page: +page,
    }
  }
  const { data, error, loading } = useQuery<{posts?: AllPostsResponse} & {user?: UserResponse}>(Query, QUERY_OPTIONS);
  const pageCount = Math.ceil(data?.user?.posts?.meta?.totalCount ? data?.user?.posts?.meta?.totalCount/4 : data?.posts?.meta?.totalCount ? data?.posts?.meta?.totalCount/4 : 1 );

  if(loading) return <p>Loading...</p>
  if(error) return <p>{`error: ${error}`}</p>

  return (
    <div>
      <Head>
        <title>Viable Ventures</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
        <link rel="stylesheet" href="https://fonts.googleapis.com/css?family=Roboto" />
      </Head>
      <Header></Header>
      <StyledContainer>
        <Sidebar />
        <Main>
          <FilterWrapper>
            <Pagination pageCount={pageCount} page={page}></Pagination>
          </FilterWrapper>
          <Articles>
            {data?.posts?.data ? data?.posts?.data?.map((post) => (
              <ItemWrapper key={post.id}>
                <BlogItem username={post?.user?.name || ''} post={post} />
              </ItemWrapper>
            )) : data?.user?.posts.data.map((post) => (
              <ItemWrapper key={post.id}>
                <BlogItem username={data?.user?.name ? data?.user?.name : ''} post={post} />
              </ItemWrapper>
            ))}
          </Articles>
        </Main>
      </StyledContainer>
    </div>
  )
}

export default Home
